int		ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long k;

	k = nb;
	if (k < 0)
	{
		k = k * -1;
		ft_putchar('-');
	}
	if (k >= 10)
		ft_putnbr(k / 10);
	ft_putchar('0' + k % 10);
}
